                            <h4>
                              
                               Maramureș (în maghiară Máramaros megye, în ucraineană Мараморо́щина, în germană Kreis Marmarosch) este un județ format din teritorii ale regiunilor istorice Maramureș, Ardeal și Crișana (respectiv Partium), situat în nord-vestul României. A fost înființat în anul 1968 prin reorganizarea teritorială a Regiunii Maramureș (din raioanele Șomcuta Mare, Baia Mare, Sighet, Lăpuș și Vișeu). Cea mai mare parte a teritoriului județului de azi a făcut parte mai devreme din Județul Maramureș (interbelic), respectiv din Comitatul Maramureș (antebelic). Din punct de vedere al culturii tradiționale, actualul județ este alcătuit din patru zone (etno-folcorice) distincte: Țara Chioarului, Țara Lăpușului, Țara Maramureșului (jumătatea de nord-est a Maramureșului Istoric) și Țara Codrului (partea de sud-vest), la care se adaugă Zona Metropolitană Baia Mare. Reședința și centrul cultural, educațional și economic a județului este municipiul Baia Mare.
                        <hr>
                        
                           Județul Maramureș este situat în partea de nord a țării, fiind delimitat de județele Satu-Mare, Sălaj, Cluj, Bistrița-Năsăud și Suceava, respectiv la nord fiind delimitat de frontiera cu Ucraina, având o suprafată de 6.215 km² (2,6% din suprafața țării) și un relief variat ca morfologie si complex din punct de vedere geologic.
<br>
Zona montană aparținând Carpaților Orientali reprezintă 43%, zona colinară (dealuri, podișuri și piemonturi) circa 30%, iar zona joasă (depresiuni, lunci și terase) restul de 27% din suprafața județului. Principalele unități montane sunt: Munții Rodnei (cei mai înalți), Munții Maramureșului și lanțul vulcanic Igniș-Gutâi-Țibleș.
<br>
Rețeaua hidrografică este reprezentată de principalele răuri: Tisa, Vișeu, Iza, Lăpuș și Someș.
                        <hr>
                          
                              Vegetația și fauna sunt caracterizate printr-o varietate de specii în functie de altitudine, unele avand caracter endemic munților Rodnei. Speciile de animale sunt bine reprezentate, prezentând o mare varietate în funcție de altitudine: în zona alpină de capra neagră, marmota, acvila de stâncă, în jnepeniș de cocoșul de mesteacăn, iar în pădurile de conifere de râs, cocoșul de munte, ursul brun, cerbul carpatin și altele. In râurile de munte, Tisa, Vișeu, Ruscova și Vaser, alături de păstrav și lipan trăiește cea mai valoroasa specie de salmonide, lostrița.
<br>
Datorită acestor bogății naturale, în judetul Maramureș au fost declarate prin Decretul 204/1997 și H.C.J 37/1994, 20 de obiective naturale-rezervații naturale de interes național, iar Rezervația științifică Pietrosu Mare (cuprinsă în aria teritorială a Parcului Național Rodna) este inclusă în programul mondial al UNESCO - „Omul și Biosfera”, alături de Delta Dunării și Parcul Național Retezat.
                        <hr>
                          
                              Cursurile de apă: au o direcție nord - vest, sud - est, fiind formate din Prut la est și Siret la vest, Bașeu și Jijia în centru, cu afluenții importanți: Sitna, Miletin, Dresleuca ce formeaza culoare depresionare largi cu lunci extinse ce brazdează județul, determinând crearea artificială a peste 150 iazuri, utilizate pentru echilibrarea debitelor, irigații, alimentare cu apă, piscicultură. În zona localităților Stânca - Costești a fost construit un important nod hidrotehnic, realizându-se una din cele mai mari acumulări din țară, cu un volum de 1,5 miliarde mc apă, cu o suprafață de 1600 ha și o lungime de 70 km
                        <hr>
                         
