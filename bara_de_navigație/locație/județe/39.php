                            Suprafața totală a județului Călărași, de 5,088 km², face parte din partea de sud a câmpiei Bărăganului. Județul Călărași face parte din bazinul hidrografic al fluviului Dunăre, pricipalul curs de apă al județului. Alte cursuri de apă importante sunt: râul Argeș, care se varsă în Dunăre la Oltenița, râul Dâmbovița, care se varsă în Argeș la Budești și salba de lacuri Mostiștea care se varsă în Dunăre la Mănăstirea. La Călărași, Dunărea se divide în două brațe: Dunărea Veche și Borcea, pe malul căreia se află Călărașiul, între acestea formându-se mlaștini și lacuri. Cele mai importante lacuri ale județului sunt: lacul Mostiștea (parte a salbei de lacuri Mostiștea), lacul Gălățui și lacul Călărași (în prezent rămânând doar o mică parte din vechea suprafață a lacului în urma desecărilor și îndiguirilor).
