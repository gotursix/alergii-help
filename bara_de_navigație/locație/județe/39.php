                                Călărași este un județ în sud-estul regiunii Muntenia din România. Reședința sa este municipiul Călărași. În județul Călărași sunt: 305.536 români; 18.343 rromi; 469 turci; 97 maghiari; 34 ruși/lipoveni; 26 germani; 18 bulgari; 17 ucrainieni; 9 italieni; 8 greci; 7 chinezi; 4 polonezi și 4 evrei; 3 sârbi și 3 ceangăi plus câte un tătar, ceh, slovac, croat și armean. 27 au alte etnii, iar 7 nu și-au declarat etnia.
