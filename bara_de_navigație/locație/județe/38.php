                            <h4>
                         
                                Județul Ilfov (Sectorul agricol Ilfov înainte de 1997) este un județ în Muntenia, România, ce înconjoară municipiul București. Reședința oficială este orașul București[1] și majoritatea instituțiilor județene își au sediul acolo.
                        <hr>
                        
                         Urme ale existenței umane descoperite pe raza județului Ilfov, mai ales în zona Căldărușani, Glina și Chiajna, lângă București, datează din paleolitic și neolitic. Trebuie de asemenea menționate vestigiile dacice scoase la lumină în zona Pantelimon, Snagov, Măgurele și Bragadiru. Bogăția descoperirilor arheologice (relicve, inscripții etc.) este dovada locuirii neîntrerupte a zonei de către daco-romani, care mai târziu au format Principatul Țara Românească, prin unirea cnezatelor și voievodatelor (primele organizări statale) în secolele XII-XIV. Încă de la întemeire, la sfârșitul secolului al XIV-lea, sub domnia lui Basarab I, multe așezări și orașe au prins viață până în secolul XVI, cum ar fi Snagov, Chitila, Afumați sau Tâncăbești și care au supraviețuit până în zilele noastre. Numele de Ilfov este pentru prima data menționat într-un act de donație din 1482 al domnitorului Vlad Călugărul către Mănăstirea Snagov.
                        <hr>
                          
                              Județul Ilfov, cel mai mic județ al României ca suprafață, se află în Câmpia Română. În monografia "Județele României" din 1972 sunt menționate ca vecine ale Județului Ilfov județele: Ialomița la est, Buzău, Prahova și Dâmbovița la nord, Teleorman la vest și Bulgaria la sud. De asemenea, se precizează că acesta se întinde pe o suprafață de 8.225 km², fiind în dimensiunea pe care o deținea atunci, al cincilea ca mărime din România. Nouă ani mai târziu, al cincilea județ al României ca suprafață, a fost dezmembrat: partea de sud-vest a format județul Giurgiu, partea de nord-vest a fost alipită la Dâmbovița, partea de nord-est a fost trecută la Ialomița iar partea de sud-est a intrat în componența noului județ Călărași. Comunele care înconjurau zona capitalei au fost subordonate Municipiului București, sub numele de Sectorul Agricol Ilfov.<br>

În 1997, legea nr. 50/97 a reînființat județul Ilfov, în limitele fostului Sector Agricol Ilfov, scoțându-l totodată de sub tutela Municipiului București. Județul Ilfov cuprinde 34 de așezări rurale și opt orașe: Buftea, Otopeni, Voluntari, Bragadiru, Chitila, Măgurele, Popești-Leordeni, Pantelimon, cu peste 1.593 km².<br>

Județul se găsește în regiunea de sud-est, chiar în centrul Câmpiei Române, lângă București. Coordonatele sale goegrafice sunt 44°17’ – 44°46’ latitudine nordică și 25°52’ – 26°27’ longitudine estică. În ziua de astăzi, Ilfov se învecinează cu județele Prahova (la nord), Dâmbovița (la nord-vest), Giurgiu (la sud și vest), Călărași (la sud-est) și Ialomița (la nord-est). Clima este temperat-continentala cu o medie anuală a precipitațiilor de 460–500 mm. Râuri importante sunt Dâmbovița și Colentina (care aprovizionează cu apă orașul Buftea), iar lacuri importante sunt Cernica, Snagov și Căldărușani în partea nordică a județului. Județul este străbătut și de râul Ialomița împreună cu afluentul său Gruiu.
