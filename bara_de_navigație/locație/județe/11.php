                            <h4>
                              
                               Mureș (în maghiară Maros megye, în germană Kreis Mieresch) este un județ în regiunea Transilvania din România. A fost înființat în anul 1968 prin reorganizarea teritorială a regiunii Regiunii Mureș-Autonomă Maghiară și a Regiunii Brașov (din raioanele Reghin, Târgu Mureș, Luduș, Târnăveni și Sighișoara). Cea mai mare parte a teritoriului județului de azi a făcut parte mai devreme din Județul Mureș (interbelic), din Comitatul Mureș-Turda (antebelic), iar mai devreme din Scaunul Secuiesc al Mureșului, Scaunul Săsesc a Sighișoarei și din Comitatul Turda. Din punct de vedere al culturii tradiționale, actualul județ face parte din mai multe zone (etno-folcorice și istorice) distincte, precum Ținutul Secuiesc, Pământul crăiesc etc. Reședința și centrul cultural, educațional și economic a județului este municipiul Târgu Mureș. Are o suprafață totală de 6.714 km² care reprezintă 2,8% din suprafața totală a țării. Numele județului provine de la râul Mureș, râu care străbate județul de la NE la SV.
                        <hr>
                        
