                            <h4>
                              
                               Bistrița-Năsăud (în maghiară Beszterce-Naszód megye, în germană Kreis Bistritz-Nassod) este un județ în Transilvania, România. A fost înființat în anul 1968 prin reorganizarea teritorială a Regiunii Cluj (din raioanele Bistrița și Năsăud). Cea mai mare parte a teritoriului județului de azi a făcut parte mai devreme din Județul Năsăud (interbelic), respectiv din Comitatul Bistrița-Năsăud (antebelic). Reședința și centrul cultural, educațional și economic a județului este municipiul Bistrița. Județul este împărțit, din punct de vedere administrativ, într-un municipiu, 3 orașe și 58 de comune cu 235 de sate. Se învecinează cu județele Cluj, la vest, Maramureș, la nord, Suceava, la est și Mureș la sud.
                        <hr>
                        
                             Parcul dendrologic Arcalia (la 17 km vest de Bistrița, în apropierea satului Arcalia) - se întinde pe mai mult de 16 ha și adăpostește peste 150 de specii de copaci provenind din diverse zone ale lumii (salcâm japonez, brad argintiu, molid caucazian etc).
                        <hr>
                        
                           Stema județului Bistrița-Năsăud se compune dintr-un scut sfertuit; în primul cartier, pe fond de azur, se află o acvilă naturală, ținând în ciocul roșu o cruce ortodoxă de aur și având pe piept un scut de argint cu inițialele VRR (Virtus Romana Rediviva); la baza acvilei se află o carte deschisă, de culoare albă. În cartierul secund, pe câmp de aur, este reprezentată, în culoare naturală, lupoaica cu Romulus și Remus. În cartierul trei, pe fond roșu, este conturat un scut cu bordură de aur, cu marginile neregulate, al cărui câmp este, de asemenea, roșu; în interiorul acestuia se află capul de bour, în culoare naturală, având între coarne o stea de aur, formată din șase raze. În cartierul patru, pe fond de argint, se află un personaj purtând costum popular, ținând în mâna dreaptă o suliță, iar în cea stângă, un scut cu ornament floral.
                        <hr>
                         
                             Cele mai întâlnite alergii, prezente aici, sunt: alergia la fumul de țigară, alergiile alimentare, alergia la polen.
