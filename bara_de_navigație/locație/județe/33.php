Există două posibile origini ale cuvântului Mehedinți: una latină, ce ar deriva din anticul "Mediam", colonie romană de lângă localitatea "Mehadia" (Caraș-Severin) sau maghiară: "méhészkedés" - stupărit ("méhek" însemnând albină). Imaginea albinelor în heraldica ținutului regiunii Mehedințiului pare să confirme cea de-a doua origine etimologică.<br>

Conform istoricului Bogdan Petriceicu Hasdeu, în a sa Istorie Critică a Românilor, numele de Mehedinți vine de la Mehedinski, un termen slav desemnând teritoriul din jurul orașului Mehadia, oraș aflat pe teritoriul județului vecin Caraș Severin, la granița cu Mehedințiul.
