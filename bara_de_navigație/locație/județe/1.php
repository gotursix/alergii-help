                            <h4>
                               Satu Mare (în maghiară Szatmár megye, în germană Kreis Sathmar) este un județ în nord-vestul României. A fost înființat în anul 1968 prin reorganizarea teritorială a fostei Regiuni Maramureș (din raioanele Satu Mare, Carei și Negrești-Oaș). Cea mai mare parte a teritoriului județului de azi a făcut parte mai devreme din Județul Satu-Mare (interbelic), respectiv din Comitatul Sătmar (antebelic). Reședința și centrul cultural, educațional și economic a județului este municipiul Satu Mare.
                       
                        <hr>
                          Situat în partea de nord-vest a României, județul Satu Mare (4.405 km pătrați) este una din porțile principale ale României, având granițe cu Ungaria și Ucraina. Având un climat temperat-continental, județul Satu Mare a fost populat cu mai mult de 100.000 de ani î.Hr., oamenii locuind pe terasele înalte din Țara Oașului (Remetea-Oaș, Boinești, Călinești-Oaș etc).<br>

În această regiune dacii au creat în decurs de circa 1000 de ani o civilizație dezvoltată, în special la Medieșu-Aurit. Situat la intersecția unor drumuri comerciale importante, având nenumărate resurse naturale, județul Satu-Mare (atestat documentar din 1181) a avut o istorie tumultoasă, cu evenimente memorabile. Puternice cetăți ale căror ruine se află în Satu Mare, Ardud, Medieșu Aurit, Tămășeni, Carei, etc., biserici romanice (Acâș, secolul al XIII-lea), biserici gotice (Ardud, Enu-Sâncrai, Beltiug).<br>

Personalități: episcopul Grigore Maior, fondator de școli, Ferenc Kölcsey, luptător pentru libertate și justiție socială, preotul Vasile Lucaciu, memorandist, poetul Ady Endre, pictorul Aurel Popp.<br>

Secolul al XX-lea a marcat profund județul Satu Mare prin cele două războaie mondiale și prin Unirea Transilvaniei cu România la 1 decembrie 1918. Creat de sculptorul Vida Geza în 1964, monumentul de la Carei este reprezentativ pentru secolul al XX-lea, un secol cu războaie și catastrofe naturale, dar de asemenea un secol de mari progrese și speranțe după Decembrie 1989. 
                        
